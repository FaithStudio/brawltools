        public static void DeleteHardcodedFiles()
        {
            if (File.Exists(ShadowMaterial.Name))
            {
                File.Delete(ShadowMaterial.Name);
            }
            if (File.Exists(BulborbShadowMaterial.Name))
            {
                File.Delete(BulborbShadowMaterial.Name);
            }
